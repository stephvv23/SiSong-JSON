    public void setLbIdSong(int idSong) {
        this.lbIdSong.setText(String.valueOf(idSong));
    }// Cargar el ID en el label

    public Song getSong() {
        return new Song(Integer.parseInt(this.lbIdSong.getText()),
                this.txTitle.getText(),
                Double.parseDouble(this.spTime.getValue().toString()),
                this.cbMusicGenre.getSelectedItem().toString(),
                Integer.parseInt(this.spReleaseYear.getValue().toString()));
    }

    public void setSong(Song song) {
        this.lbIdSong.setText(String.valueOf(song.getIdSong()));
        this.txTitle.setText(song.getTitle());
        this.spTime.setValue(song.getTime());
        this.cbMusicGenre.setSelectedItem(song.getMusicGenre());
        this.spReleaseYear.setValue(song.getReleaseYear());
    }

    public void clean() {
        this.txTitle.setText("");
        this.spTime.setValue(0.1);
        this.cbMusicGenre.setSelectedIndex(0);
        this.spReleaseYear.setValue(2024);
    }

    public void setCbSong(String[] idSong) {
        DefaultComboBoxModel model = new DefaultComboBoxModel(idSong);
        model.insertElementAt("Selected Option", 0);
        this.cbSong.setModel(model);
    } 
    
    public void setCbMusicGenre(){
        this.cbMusicGenre.setModel(new DefaultComboBoxModel<>(new String[] {"Selected option","Pop", "Rock", "Metal", "Regueton", "Salsa", "Cumbia", "Clasica", "Hip-Hop", "Jazz", "Bachata", "Merengue"}));
    }

    public void listenCombo(ActionListener controller) {
        this.cbSong.addActionListener(controller);
    }

    public String getCbSong() {
        return this.cbSong.getSelectedItem().toString();
    }


